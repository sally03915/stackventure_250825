package com.company.java007_ex;

import java.util.Arrays;

public class ArrayEx008 {
	public static void main(String[] args) {
		// 변수  + 입력
	    String[] name={"아이언맨","헐크","캡틴","토르","호크아이"};
	    int[] kor={100,20,90,70,35};   
	    int[] eng={100,50,95,80,100};
	    int[] mat={100,30,90,60,100};
	    int[] aver=new int[5];
	    String[] pass = new String[5];
	    String[] jang = new String[5];
	    
	    // 처리
		//1. 평균구하시오.
		//ver-1		아이언맨의평균 = (아이언맨의 국어 + 아이언맨의 영어 + 아이언맨의 수학)/3
		//			헐크의평균    = (헐크의 국어    + 헐크의 영어    +헐크의 수학)/3
		//ver-2     aver[0]	    = ( kor[0] + eng[0] + math[0] )/3;		
		//   	    aver[1]	    = ( kor[1] + eng[1] + math[1] )/3;		
	    for(int i=0;i<name.length; i++){  aver[i]	    = ( kor[i] + eng[i] + mat[i] )/3; }
	    
	    //2. 합격,불합격   -  평균이 60점이상이면 합격, 아니면 불합격으로 처리하시오.
	    //ver-1    아이언맨의 평균이  60점이상이면 합격, 아니면 불합격.
	    //ver-2    if(아이언맨의 평균이  60점이상이면){ 합격}{ 아니면 불합격}
	    //ver-3    if(aver[0]>=60){ pass[0] = "합격";}{ pass[0] = "불합격";}
	    
	    for(int i=0; i<name.length; i++) {   if(aver[i]>=60){ pass[i] = "합격";}else{ pass[i] = "불합격";}  }
	    
	    //3. 장학생   - 평균 95점이상이면 장학생으로 처리하시오. 
	    //ver-1   아이언맨의 평균이 95점이상이면 장학생 / ---- 
	    //ver-2   if(아이언맨의 평균이 95점이상이면){ 장학생} else {----}
	    for(int i=0; i<name.length; i++){	if(aver[i] >= 95){ jang[i] = "장학생";} else {jang[i] = "----";}  }
	    
	    // 출력
		//	    System.out.println(Arrays.toString(name));
		//	    System.out.println(Arrays.toString(aver));
		//	    System.out.println(Arrays.toString(pass));
		//	    System.out.println(Arrays.toString(jang));
	    System.out.println(":::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::\r\n"
	    		+ "이름\t국어\t영어\t수학\t평균\t합격여부\t장학생	\r\n"
	    		+ ":::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::");
	    for(int i=0; i<name.length; i++) {
	    	System.out.printf("%s\t%d\t%d\t%d\t%d\t%s\t%s\n" ,
	    			name[i] , kor[i],eng[i] , mat[i],aver[i] , pass[i], jang[i]);
	    }
	}
}

